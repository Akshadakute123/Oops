package Com.Bridgelabz.oops.commercialdata;

public class Customer 
{
   private String customer_name;
   private int no_of_customershares;
   private int price;
public String getCustomer_name() {
	return customer_name;
}
public void setCustomer_name(String customer_name) {
	this.customer_name = customer_name;
}
public int getNo_of_shares() {
	return no_of_customershares;
}
public void setNo_of_shares(int no_of_shares) {
	this.no_of_customershares = no_of_shares;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}
   
}
